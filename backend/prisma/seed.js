async function main() {
  console.log('Seeding database...');

  // Clear existing seed dataset if it exists
  await prisma.dataset.deleteMany({ where: { name: 'Market Prices 2021–2024' } });

  const records = [];
  for (const { asset, category, prices } of assetData) {
    for (const [dateStr, value] of prices) {
      records.push({
        date: new Date(dateStr),
        assetName: asset,
        value,
        category,
      });
    }
  }

  const dataset = await prisma.dataset.create({
    data: {
      name: 'Market Prices 2021–2024',
      records: { create: records },
    },
    include: { _count: { select: { records: true } } },
  });

  console.log(`Created dataset "${dataset.name}" with ${dataset._count.records} records.`);
  console.log(`Dataset ID: ${dataset.id}`);
}

main()
  .catch((e) => { console.error(e); process.exit(1); })
  .finally(() => prisma.$disconnect());
